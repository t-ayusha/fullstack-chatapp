import React from 'react'
import { useChatStore } from '../store/useChatStore'
import { useEffect,useRef } from 'react';
import ChatHeader from './ChatHeader';
import MessageInput from './MessageInput';
import MessageSkeleton from './skeletons/MessageSkeleton';
import { useAuthStore } from '../store/useAuthStore';
import { formatMessageTime } from '../lib/utils'
import { Trash2 } from 'lucide-react';
const ChatContainer = () => {
  const {messages,getMessages,isMessagesLoading,selectedUser,subscribeToMessages,unsubscribeFromMessages} = useChatStore();
  const {authUser}=useAuthStore();
  const messageEndRef=useRef(null); //to scroll to bottom when new message arrives

  
  useEffect(()=>{
    getMessages(selectedUser._id)

    subscribeToMessages();//subscribe to messages when selected user changes
    return()=>{
      unsubscribeFromMessages();//unsubscribe when component unmounts or selected user changes
    }
  },[selectedUser._id,getMessages,subscribeToMessages]);

  useEffect(()=>{
    //scroll to bottom when messages change
    if(messageEndRef.current && messages) { 
      messageEndRef.current.scrollIntoView({behavior:"smooth"});
    }
  },[messages])


  if(isMessagesLoading) {return (
    <div className="flex-1 flex flex-col overflow-auto">
      <ChatHeader/>
      <MessageSkeleton/>
      <MessageInput/>
    </div>
  ) }

  return (
    <div className="flex-1 flex flex-col overflow-auto">
      <ChatHeader/>

      {/* Messages */}
      <div className="flex-1 overflow-y-auto p-4 space-y-4">
        {messages.map((message)=>(
          <div 
          key={message._id} 
          className={`chat ${message.senderId===authUser._id? "chat-end":"chat-start"}`}
          ref={messageEndRef}>
            <div className="chat-image avatar">
              <div className="size-10 rounded-full border">
                <img src={message.senderId===authUser._id?authUser.profilepic||"/avatar.png":selectedUser.profilepic||"/avatar.png"} alt="profile pic" />

              </div>
            </div>
            <div className="chat-header mb-1">
              <time className="text-xs  opacity-50 ml-1">
                {formatMessageTime(message.createdAt)}
              </time>
            </div>
            <div className="chat-bubble flex flex-col">
              {message.image&&(
                <img src={message.image} alt="attachment" className="sm:max-w-[200px] rounded-md mb-2"/>
              )}
              {message.text&&<p>{message.text}</p>}
              {message.senderId === authUser._id && (
                <button
                  onClick={() => deleteMessage(message._id)}
                  className="btn btn-sm btn-ghost self-end mt-1 opacity-50 hover:opacity-100"
                >
                  <Trash2 size={14} />
                </button>
              )}
            </div>
          </div>
        ))}
      </div>

      <MessageInput />
    </div>
  )
}

export default ChatContainer